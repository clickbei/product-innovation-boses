using BosesApp.Core.Interfaces;
using System.Diagnostics;

namespace BosesApp.Core.Services;

/// <summary>
/// No-op SMS gateway used during local development and CI.
/// Prints the full message to the debug console — no real SMS is sent.
/// Register this as ISmsGateway in MauiProgram.cs for zero-config demos.
/// </summary>
public class SimulatedSmsGateway : ISmsGateway
{
    public string ProviderName => "Simulated (Debug Log)";

    public Task<SmsResult> SendAsync(string toPhone, string message)
    {
        Debug.WriteLine("???????????????????????????????????????????????????");
        Debug.WriteLine($"? [SimulatedSMS] TO  : {toPhone}");
        Debug.WriteLine($"? [SimulatedSMS] MSG : {message}");
        Debug.WriteLine("???????????????????????????????????????????????????");

        return Task.FromResult(new SmsResult
        {
            Success   = true,
            MessageId = $"SIM-{Guid.NewGuid():N}"[..12]
        });
    }
}
